class Probe {
    // use it as private method.
    constructor(name, ipBuffer, tcpBuffer, portOpening, port = null) {
        this.name = name;
        this.ipBuffer = ipBuffer;
        this.tcpBuffer = tcpBuffer;
        this.portOpening = portOpening;
        this.port = port;
    }

    static fromName(name, portOpening) {
        return new Probe(name, ipBuffers[name], tcpBuffers[name], portOpening);
    }

    setPort(port) {
        this.port = port;
        return this;
    }
}

const probes = {
    "T2": Probe.fromName('T2', true),
    "T3": Probe.fromName('T3', true),
    "T4": Probe.fromName('T4', true),
    "T5": Probe.fromName('T5', false),
    "T6": Probe.fromName('T6', false),
    "T7": Probe.fromName('T7', false),
};

const setPort = ([opening, closed]) => {
    for (const name of Object.keys(probes)) {
        const probe = probes[name];
        if (probe.portOpening)
            probes[name] = probe.setPort(opening);
        else
            probes[name] = probe.setPort(closed);
    }
    return probes;
};

module.exports = {
    setPort: setPort,
}
